package cProject.movie.vo;

public class StarVO {
	
	private int starNo;
	private String author;
	private int bno;
	private int star;
	
	public int getStarNo() {
		return starNo;
	}
	public String getAuthor() {
		return author;
	}
	public int getBno() {
		return bno;
	}
	public int getStar() {
		return star;
	}
	public void setStarNo(int starNo) {
		this.starNo = starNo;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public void setStar(int star) {
		this.star = star;
	}
	
}
